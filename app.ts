   // Comparision Operators  //

let num1:number = 10;
let num2:number = 15;
console.log(
    num1 == num2
)                     // false

let num3:String = "apple";
let num4:String = "apple";

console.log(
    num3 == num4
);        // true


let num5:number = 10
let num6:number = 9

console.log(
    num5 > num6
);          //true


let num7:number = 10
let num8:number = 9

console.log(
    num7 != num8
);     //true

let num01:number = 23;
let num02:number = 14;
console.log(
    num01 == num02
);             // false


let num03:number = 89;
let num04:number = 90;

console.log(
    num03 !> num04
);           //false

let num9:number = 45
let num10:number = 34
console.log(
    num9 != num10
);     // true
